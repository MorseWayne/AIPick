# 统一管理所有提供给大语言模型的系统提示词 (System Prompts)

from typing import Optional


def build_needs_analysis_prompt(
    round_num: int,
    max_rounds: int,
    must_ask: bool = False,
) -> str:
    """
    根据当前对话轮次动态生成需求分析提示词。
    LLM 从完整对话历史中自行提取已收集信息，避免注入过期上下文导致重复提问。
    """

    base = (
        "你是一个经验丰富的购物顾问。分析对话上下文，提取用户购买意图，判断是否需要追问。\n"
        "关注以下维度：\n"
        "1. 商品类目 - 具体到子类（如「面霜」而非「护肤品」）\n"
        "2. 预算范围 - 用户主动提及的价格区间\n"
        "3. 核心需求 - 2~4 个具体功能/特性要求\n"
        "4. 用户画像 - 年龄段、性别、肤质/体型等\n"
        "5. 使用场景 - 日常/送礼/特定活动\n"
        "6. 品牌偏好 - 品牌倾向或排斥\n"
        "7. 过往痛点 - 同类产品的不满之处"
    )

    follow_up_rules = (
        "\n\n### 追问规则\n"
        "- 每次只问一个问题，自然亲切，像朋友聊天\n"
        "- ⚠️ 禁止「双边提问」：一个问题只问一个方向。错误示例：「有倾向或想避开的品牌吗？」——用户不知道该答喜欢的还是讨厌的。正确做法：先问「有没有特别喜欢的品牌？」，下一轮再问排斥的\n"
        "- 根据已知信息选最有价值的追问方向（如说「买护肤品」→先问肤质；「送妈妈」→先问年龄）\n"
        "- 给出 follow_up_reason（一句话解释为什么需要此信息）\n"
        "- follow_up_options 提供 2~5 个简短选项（每个≤10汉字），如 [\"油皮\",\"干皮\",\"混合皮\",\"敏感肌\"]\n"
        "- ⚠️ follow_up_options 必须是 follow_up_question 的直接回答选项！例如问「质地偏好」→选项应为 [\"清爽型\",\"滋润型\",\"都可以\"]，绝不能放无关维度的选项（如使用场景）\n"
        "- 不重复追问已回答的信息\n"
        "- 追问优先级：人群特征/肤质 > 预算 > 具体需求 > 品牌偏好"
    )

    if must_ask:
        sufficiency_check = (
            "\n\n### is_sufficient\n"
            "⚠️ 本轮必须继续追问，is_sufficient 必须为 false。\n"
            "请务必生成一个有针对性的 follow_up_question，深入了解用户需求。"
        )
    else:
        sufficiency_check = (
            "\n\n### is_sufficient 判断（全部满足才可为 true）\n"
            "□ 商品类目已具体到子类\n"
            "□ 用户主动提及了预算（不可自行假设）\n"
            "□ 至少 2 条用户明确表达的具体核心需求（「好用」「质量好」不算）\n"
            "□ 用户画像或使用场景至少了解其一"
        )

    if round_num == 0:
        context_block = (
            "\n\n### 当前状态：初始分析\n"
            "这是用户的第一条消息，通常信息不充分，请仔细分析后优先追问最关键的缺失维度。"
        )
    else:
        context_block = f"\n\n### 当前状态：第 {round_num + 1}/{max_rounds} 轮对话"
        context_block += (
            "\n请通读完整对话历史，自行提取各维度的收集情况，然后对照以下清单判断还缺什么：\n"
            "□ 商品类目（具体到子类）\n"
            "□ 预算范围\n"
            "□ 核心需求（≥2条具体功能/特性）\n"
            "□ 用户画像或使用场景（至少其一）\n"
            "只追问对话中确实从未提及过的维度，绝对不要重复追问用户已回答的内容。"
        )

        if round_num >= max_rounds - 1:
            context_block += "\n⚠️ 即将达到最大追问轮数，若关键信息已基本够用可标记为 sufficient。"

    tail = "\n\n即使信息不完整也请尝试生成 keywords 和 search_queries。以 JSON 格式返回。"

    return base + follow_up_rules + sufficiency_check + context_block + tail


def build_batch_questions_prompt(num_questions: int = 5) -> str:
    """
    根据用户初始输入，一次性生成指定数量的追问问题。
    LLM 先分析输入中已有信息，再针对缺失维度生成追问。
    """
    return (
        f"你是一个经验丰富的购物顾问。请仔细分析用户的初始需求描述，提取已知信息，"
        f"然后一次性生成 {num_questions} 个精准的追问问题，用于全面了解用户需求。\n\n"
        "### 分析维度\n"
        "1. 商品类目 - 具体到子类（如「面霜」而非「护肤品」）\n"
        "2. 预算范围 - 用户的价格区间\n"
        "3. 核心需求 - 2~4 个具体功能/特性要求\n"
        "4. 用户画像 - 年龄段、性别、肤质/体型等\n"
        "5. 使用场景 - 日常/送礼/特定活动\n"
        "6. 品牌偏好 - 品牌倾向或排斥\n"
        "7. 过往痛点 - 同类产品的不满之处\n\n"
        "### 生成规则\n"
        "- 先分析用户输入中已包含的信息，只针对**缺失或不明确的维度**生成追问\n"
        f"- 必须恰好生成 {num_questions} 个问题\n"
        "- 每个问题只问一个方向，自然亲切，像朋友聊天\n"
        "- ⚠️ 禁止「双边提问」：一个问题只问一个方向。"
        "错误示例：「有倾向或想避开的品牌吗？」——用户不知道该答喜欢的还是讨厌的。"
        "正确做法：「有没有特别喜欢的品牌？」\n"
        "- 每个问题的 options 提供 2~5 个简短选项（每个≤10汉字），选项必须是该问题的直接回答\n"
        "- 不要追问用户已经在输入中明确表达的信息\n"
        "- 每个问题的 allow_multiple 字段：互斥性选项（预算范围、年龄段、性别）设为 false（单选）；"
        "可叠加选项（功能需求、品牌偏好、痛点、购买渠道）设为 true（多选）\n"
        "- 问题按重要性排序：人群特征/肤质 > 预算 > 具体需求 > 品牌偏好 > 使用场景 > 痛点\n\n"
        "请以 JSON 格式返回。"
    )


def build_final_synthesis_prompt() -> str:
    """
    综合用户初始输入和所有追问回答，生成最终的完整需求分析。
    """
    return (
        "你是一个经验丰富的购物顾问。请综合分析用户的初始需求描述和所有追问回答，"
        "提取完整的购买需求信息。\n\n"
        "请从对话历史中提取以下维度的信息：\n"
        "1. 商品类目 - 具体到子类（如「面霜」而非「护肤品」）\n"
        "2. 预算范围 - 用户明确提及的价格区间\n"
        "3. 核心需求 - 至少 2 条具体功能/特性\n"
        "4. 用户画像 - 年龄段、性别、肤质/体型等\n"
        "5. 使用场景 - 日常/送礼/特定活动\n"
        "6. 品牌偏好 - 品牌倾向或排斥\n"
        "7. 过往痛点 - 同类产品的不满之处\n\n"
        "### 要求\n"
        "- is_sufficient 设为 true\n"
        "- 即使某些维度用户未明确回答（如跳过了问题），也请基于已有信息合理推断\n"
        "- 生成精准的 keywords（搜索关键词，用空格分隔核心词）\n"
        "- 生成 2~3 个 search_queries（多角度补充搜索查询）\n"
        "- 不需要生成 follow_up_question、follow_up_reason、follow_up_options\n\n"
        "以 JSON 格式返回。"
    )


def build_web_search_prompt(user_context: str, pain_points: list[str]) -> str:
    sys_prompt = f"""你是一个专业的商品调研分析师，擅长从全网信息中筛选出最适合用户需求的商品。

用户需求深度画像：
{user_context}

请你联网搜索当前市场上符合以上需求的商品，**优先查找该品类的权威排行榜和畅销榜**，重点参考以下维度的信息：
1. **销量排行榜（最重要）**：优先搜索京东、天猫、拼多多等主流电商平台的品类销量排行榜，筛选**销量高、用户评价数量多**的商品
2. **用户评价质量**：重点关注**好评率高（≥95%）、差评率低**的商品，排除差评较多或口碑争议大的产品
3. **专业评测**：科技媒体的深度评测结论（如什么值得买、中关村在线、ZEALER等）
4. **性价比分析**：同价位段竞品对比

⚠️ 筛选原则：
- **高销量优先**：优先推荐月销量靠前的商品，销量是市场认可度的最直接体现
- **高好评优先**：评价数量多且好评率高的商品更值得信赖
- **差评预警**：如果某商品差评中频繁出现某类问题，应在 highlights 中如实反映
- 不要推荐销量极低或刚上市缺乏用户反馈的产品（除非是明确的新品且专业评测优异）

请特别注意用户的画像特征和使用场景，筛选出真正适合该用户的 3~5 款商品（而非泛泛的热销品）。
{('注意：用户有以下过往痛点，推荐时请务必避开类似问题的产品：' + ', '.join(pain_points)) if pain_points else ''}

⚠️ 关于 search_keyword_for_xhs 字段的特别要求：
该关键词将用于小红书站内搜索，请务必遵循以下规则：
- 关键词必须**简短精准**，总共 2~4 个词，不要超过 10 个汉字
- 格式参考："品牌名 产品型号/系列名"，例如 "珀莱雅红宝石套装"、"ThinkPad X1 Carbon"
- 不要加 "测评"、"推荐"、"实测" 等后缀，也不要加用户人群词（如 "中年"、"学生"）
- 不要放价格信息
- 目标是让小红书搜索能精准命中该商品的真实用户笔记

⚠️ 关于 raw_search_evidence 字段的特别要求：
请将你在联网搜索中获取到的**关键数据点和信息来源**汇总在此字段中，包括但不限于：
- 各电商平台的具体销量数据（如："京东月销 10 万+""天猫好评率 98%"）
- 专业评测网站的具体评分（如："什么值得买评分 8.7""中关村在线推荐指数 9.2"）
- 价格趋势和促销信息（如："近期大促价 3999 元""历史最低价 3799"）
- 任何有助于后续综合分析的具体数据支撑
这些原始数据将被传递给后续的综合分析阶段，帮助做出更精准的评估。

### 重要
请以 JSON 格式输出。"""
    return sys_prompt.strip()


def build_comprehensive_analysis_prompt(user_summary: str) -> str:
    sys_prompt = f"""你是一个精通全网商品测评的数据分析师，以客观、专业著称。

用户深度画像：
{user_summary}

下面是两部分数据：
1. 【全网搜索结果】：来自各大电商平台、专业评测网站的综合信息（销量排行、专业评测、性价比对比）。
2. 【小红书真实反馈】：来自小红书的真实用户笔记和评论（更贴近真实使用体验）。

请综合两部分信息进行分析，严格遵循以下要求：

### 分析步骤（请严格按此顺序执行）：

**Step 1 - 需求匹配检查：**
逐条检查用户的核心需求(core_needs)，判断每个候选商品是否满足。
将分析结果填入 needs_match_detail 字段，格式如："拍照需求✅ 徕卡调色出色；续航需求⚠️ 中等水平；价格✅ 在预算范围内"

**Step 2 - 痛点回避检查：**
逐条检查用户的过往痛点(pain_points)，判断候选商品是否可能触发类似问题。
如果商品可能触发用户的痛点，必须在 cons 中明确标注。

**Step 3 - 综合评分：**
基于以上分析 + 全网评测 + 小红书反馈，给出最终评分。

### 分析原则
- 全网信息作为"硬实力基准"（参数配置、价格竞争力、专业评测成绩）
- 小红书反馈作为"软实力验证"（真实使用体验、用户满意度、隐藏槽点）
- **忽略水军特征明显的评论**：内容过短（如仅"好用""推荐"）、高度雷同、广告痕迹重的评论
- **侧重有具体描述的长评论**：包含使用时长、肤质/体验细节、前后对比的评论权重更高

### 横向对比要求（非常重要）
- 明确指出各候选商品之间的**核心差异化特点**，不要让每款商品的描述雷同
- 在 summary 中给出「什么类型的用户更适合选这款」的差异化建议
- 推荐指数之间应有合理的拉开度（至少 5 分以上差距），除非商品确实在各维度非常接近
- 避免所有商品都给出 85~95 分的"安全区间"，要敢于做出区分

### 小红书数据不足时的处理策略
- 如果某商品在小红书上缺少真实反馈数据，**不要因此大幅降低其推荐指数**
- 而应基于全网搜索数据给出合理评估，并将 confidence_level 设为 '低'
- 在 summary 中明确标注"该商品在小红书上的真实用户反馈较少，推荐置信度有限，评分主要基于全网评测数据"
- 全网评测数据充足但小红书不足时，推荐指数最多下调 5~10 分（而非大幅度惩罚）

### 输出质量要求
- pros 和 cons 中应引用小红书真实用户的具体反馈（如："多位用户反映用了两周后斑淡了"），而不是泛泛的描述
- 如果某商品在小红书上缺少真实反馈数据，pros/cons 中应基于全网评测数据给出分析
- positive_rate 和 negative_rate 应基于实际收集到的评论样本估算，不要凭空编造
- cost_performance 应结合实际价格与同类竞品做横向对比
- recommendation_index 应综合考虑全网评测 (40%) + 小红书好评度 (30%) + 性价比 (20%) + 需求匹配度 (10%)

请以 JSON 格式输出符合 RecommendationReport 格式的综合推荐报告。"""
    return sys_prompt.strip()
